package de.hybris.platform.solrfacetsearch.config.exceptions;

public class FacetConfigExportException extends Exception
{
    private static final long serialVersionUID = 1L;


    public FacetConfigExportException()
    {
    }


    public FacetConfigExportException(String message, Throwable cause)
    {
        super(message, cause);
    }


    public FacetConfigExportException(String message)
    {
        super(message);
    }


    public FacetConfigExportException(Throwable cause)
    {
        super(cause);
    }
}
