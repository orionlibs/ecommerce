package de.hybris.platform.solrfacetsearch.indexer.exceptions;

public class IndexUpdateException extends Exception
{
    public IndexUpdateException()
    {
    }


    public IndexUpdateException(String message, Throwable cause)
    {
        super(message, cause);
    }


    public IndexUpdateException(String message)
    {
        super(message);
    }


    public IndexUpdateException(Throwable cause)
    {
        super(cause);
    }
}
