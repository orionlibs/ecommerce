package de.hybris.platform.solrfacetsearch.indexer.exceptions;

public class ExporterException extends IndexerException
{
    public ExporterException()
    {
    }


    public ExporterException(String message, Throwable cause)
    {
        super(message, cause);
    }


    public ExporterException(String message)
    {
        super(message);
    }


    public ExporterException(Throwable cause)
    {
        super(cause);
    }
}
