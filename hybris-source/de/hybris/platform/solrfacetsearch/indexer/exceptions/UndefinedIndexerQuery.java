package de.hybris.platform.solrfacetsearch.indexer.exceptions;

public class UndefinedIndexerQuery extends IndexerException
{
    public UndefinedIndexerQuery()
    {
    }


    public UndefinedIndexerQuery(String arg0)
    {
        super(arg0);
    }


    public UndefinedIndexerQuery(Throwable arg0)
    {
        super(arg0);
    }


    public UndefinedIndexerQuery(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }
}
